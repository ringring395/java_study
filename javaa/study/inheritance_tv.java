package study;
//	부모클래스(조상클래스_)
public class inheritance_tv {
	//속성(변수)
	//채널
	int channel;
	//볼륨
	int volume;
	
	//메소드(기능)
	//채널 up
	void channelup() {channel++;}
	//채널 down
	void channeldown() {channel--;}
	//볼륨 up
	void volumeup() {volume++;}
	//볼륨 down
	void volumedown() {volume--;}	

	

}//inheritance_tv닫음
